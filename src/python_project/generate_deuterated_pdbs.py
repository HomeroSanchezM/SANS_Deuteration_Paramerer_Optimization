#!/usr/bin/env python3
"""
Deuterated PDB Generator with Genetic Algorithm
================================================

Main script to generate a population of deuteration patterns,
create corresponding deuterated PDBs, run SANS simulation,
and evaluate fitness automatically.

Key design principles:
  - Each chromosome carries a ``generation`` and ``index`` attribute that
    reflect when/where it was *first created*.  These attributes are preserved
    when a chromosome is selected (tier-1) and carried over to the next
    generation, so its PDB and SANS files keep their original name forever.
  - File naming: gen{W:02d}_Chr{X:03d}_d2o{Y:03d}_deut{Z:02d}.pdb
      W = chromosome.generation  (creation generation)
      X = chromosome.index       (1-based position in creation population)
      Y = D2O percentage
      Z = number of deuterated amino acid types
  - Per-generation workflow:
      1. Generate PDB files for all NEW chromosomes (tier-2/3 only after gen 0)
      2. Run Pepsi-SANS only on those new PDB files (tier-1 SANS outputs are reused)
      3. Evaluate fitness of ALL .dat files in primus_out by filename matching
      4. Build ``sorted_indices`` (descending fitness) for display/summary/selection
      5. Validate elitism: best fitness must never decrease
      6. Clean up non-tier-1 PDB and SANS files before the next generation

Directory structure created:
    <output_dir>/
        ├── gen00_Chr001_d2o042_deut05.pdb   (stays forever if in tier-1)
        ├── gen01_Chr004_d2o030_deut08.pdb   (tier-2/3 created in gen 1)
        ├── ...
        ├── generation_00_summary.txt
        ├── best_fitness_summary.csv
        └── ref/
            ├── <protein>_total_deuteration.pdb
            └── <protein>_total_protonation.pdb

SANS data (generated by the batch script) stored in:
    <prefix>_primus_out/
        ├── gen00_Chr001_d2o042_deut05.dat   (tier-1: kept across gens)
        ├── gen01_Chr004_d2o030_deut08.dat
        └── ref/
            ├── <protein>_total_deuteration.dat
            └── <protein>_total_protonation.dat

Usage examples:
  # Minimal
  python generate_deuterated_pdbs.py input.pdb

  # Full CLI control
  python generate_deuterated_pdbs.py input.pdb -p 60 -e 5 -g 20 \\
      --d2o-var 10 -m 0.2 -c 0.9 --seed 123 --output_dir results \\
      --q-max 0.35 --ratio-threshold 0.05

  # With config file (CLI overrides INI)
  python generate_deuterated_pdbs.py input.pdb --config ga_config.ini -g 50
"""

