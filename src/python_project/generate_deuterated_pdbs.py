def create_output_directory(output_dir: Optional[str],
                             pdb_file: str):
    """Create output directory and ref/ subdirectory. Returns (output_path, ref_path)."""
    if output_dir is None:
        output_path = Path(Path(pdb_file).stem + "_deuterated_pdbs")
