def main():
    cli_args = parse_arguments()

    # Load config file if provided
    ini_cfg = None
    if cli_args.config:
        try:
            ini_cfg = load_config_ini(cli_args.config)
            logger.info(f"Loaded configuration from: {cli_args.config}")
        except (FileNotFoundError, ValueError) as e:
            logger.error(f"Config error: {e}")
            sys.exit(1)

    # Merge CLI and INI
    try:
        cfg = merge_config(cli_args, ini_cfg)
        validate_config(cfg)
    except ValueError as e:
        logger.error(f"Configuration validation failed: {e}")
        sys.exit(1)

    # Set random seed
    if cfg["seed"] is not None:
        random.seed(cfg["seed"])
        np.random.seed(cfg["seed"])
        logger.info(f"Random seed set to: {cfg['seed']}")

    # Display configuration
    logger.info("=" * 80)
    logger.info("DEUTERATED PDB GENERATION WITH GENETIC ALGORITHM")
    logger.info("=" * 80)
    logger.info(f"Source PDB           : {cfg['pdb_file']}")
    logger.info(f"Population size      : {cfg['population_size']}")
    logger.info(f"Elitism              : {cfg['elitism']}")
    logger.info(f"D2O variation rate   : ±{cfg['d2o_variation_rate']}%")
    logger.info(f"Mutation rate        : {cfg['mutation_rate']}")
    logger.info(f"Crossover rate       : {cfg['crossover_rate']}")
    logger.info(f"Generations          : {cfg['generations']}")
    logger.info(f"Q max (fitness)      : {cfg['q_max']} Å⁻¹")
    logger.info(f"Ratio threshold      : {cfg['ratio_threshold']}")
    logger.info(f"Batch script         : {cfg['batch_script']}")
    logger.info(f"Output directory     : {cfg['output_dir'] or '(auto)'}")
    if cfg['config_file']:
        logger.info(f"Config file          : {cfg['config_file']}")
    logger.info("=" * 80)

    # Check PDB file existence
    pdb_path = Path(cfg['pdb_file'])
    if not pdb_path.exists():
        logger.error(f"PDB file not found: {pdb_path}")
        sys.exit(1)

    # Check batch script
    if not Path(cfg['batch_script']).exists():
        logger.error(f"Batch script not found: {cfg['batch_script']}")
        sys.exit(1)

    # Create output directories
    output_dir, ref_dir = create_output_directory(cfg['output_dir'], str(pdb_path))

    # Path for the best fitness summary CSV (one row appended per generation)
    best_summary_path = output_dir / "best_fitness_summary.csv"

    # Create reference PDBs using the loaded restrictions
    create_reference_pdbs(str(pdb_path), ref_dir, cfg['restrictions'])

    # Initialize population generator
    generator = PopulationGenerator(
        aa_list=AMINO_ACIDS,
        modifiable=cfg['restrictions'],
        population_size=cfg['population_size'],
        elitism=cfg['elitism'],
        d2o_variation_rate=cfg['d2o_variation_rate']
    )

    tier_size = cfg['population_size'] // 3

    # ========== GENERATION 0 ==========
    logger.info("\n>>> GENERATION 0 - Creating initial population")

    # population_ordered is the population in its original order.
    # This order is used to generate filenames: population_ordered[i] -> chr{i+1}.
    # It must be kept unchanged (no in-place sorting) so that rename_tier1_files
    # can correctly look up file indices in subsequent generations.
    population_ordered = generator.generate_initial_population()
    logger.info(f"Initial population: {len(population_ordered)} unique chromosomes")

    # Generate PDBs: population_ordered[0] -> chr001, [1] -> chr002, ...
    generate_deuterated_pdbs(str(pdb_path), population_ordered, output_dir, generation=0)

    # Run SANS simulation
    primus_dir = run_batch_processing(output_dir, cfg['batch_script'])

    # Evaluate fitness (dat files alphabetical = chr001..chrN = population_ordered order)
    evaluate_fitness(
        primus_dir,
        population_ordered,
        q_max=cfg['q_max'],
        ratio_threshold=cfg['ratio_threshold'],
        deut_ref=cfg['deut_ref'],
        prot_ref=cfg['prot_ref']
    )

    # Sort for display and selection (highest fitness first)
    sorted_pop = sorted(population_ordered, key=lambda x: x.fitness if x.fitness else 0, reverse=True)
    display_population_summary(sorted_pop, generation=0)
    save_population_summary(sorted_pop, output_dir, generation=0)

    # Record best chromosome of generation 0
    save_best_fitness_summary(sorted_pop[0], generation=0, summary_path=best_summary_path)

    # ========== SUBSEQUENT GENERATIONS ==========
    for gen in range(1, cfg['generations']):
        logger.info("\n" + "=" * 80)
        logger.info(f">>> GENERATION {gen} - Population evolution")
        logger.info("=" * 80)

        # Generate next generation from sorted_pop (highest fitness first).
        # generate_next_generation returns tier1 + tier2 + tier3 in that order.
        new_population_ordered = generator.generate_next_generation(
            previous_population=sorted_pop,
            mutation_rate=cfg['mutation_rate'],
            crossover_rate=cfg['crossover_rate'],
            d2o_variation_rate=cfg['d2o_variation_rate']
        )
        logger.info(f"New population: {len(new_population_ordered)} chromosomes")

        # Tier1 chromosomes are always the first tier_size entries.
        # They are the selected individuals carried over from the previous generation.
        tier1 = new_population_ordered[:tier_size]
        tier2_and_tier3 = new_population_ordered[tier_size:]

        # Rename tier1 PDB and SANS files from gen(N-1) to gen(N).
        # CRITICAL: pass population_ordered (the previous generation's ORDERED population,
        # matching filename indices), NOT sorted_pop (sorted order would give wrong indices).
        rename_tier1_files(
            tier1=tier1,
            prev_population_ordered=population_ordered,
            output_dir=output_dir,
            primus_dir=primus_dir,
            prev_gen=gen - 1,
            new_gen=gen
        )

        # Remove remaining (non-tier1) files from the previous generation
        cleanup_non_tier1_files(
            output_dir=output_dir,
            primus_dir=primus_dir,
            generation=gen - 1
        )

        # Generate PDB files for tier2 and tier3 chromosomes only.
        # They start at index tier_size+1 so their filenames do not conflict with the
        # already-renamed tier1 files (chr001 .. chr{tier_size}).
        generate_deuterated_pdbs(
            str(pdb_path),
            tier2_and_tier3,
            output_dir,
            generation=gen,
            start_index=tier_size + 1
        )

        # Run SANS
        primus_dir = run_batch_processing(output_dir, cfg['batch_script'])

        # Evaluate fitness.
        # new_population_ordered[i] was written as chr{i+1}, so the alphabetical order
        # of dat files matches the population order.
        evaluate_fitness(
            primus_dir,
            new_population_ordered,
            q_max=cfg['q_max'],
            ratio_threshold=cfg['ratio_threshold'],
            deut_ref=cfg['deut_ref'],
            prot_ref=cfg['prot_ref']
        )

        # Sort for display and next-generation selection (highest fitness first)
        sorted_pop = sorted(
            new_population_ordered,
            key=lambda x: x.fitness if x.fitness else 0,
            reverse=True
        )
        display_population_summary(sorted_pop, generation=gen)
        save_population_summary(sorted_pop, output_dir, generation=gen)

        # Record best chromosome of this generation
        save_best_fitness_summary(sorted_pop[0], generation=gen, summary_path=best_summary_path)

        # Update population_ordered for the next iteration:
        # new_population_ordered is now the ordered population whose filenames will be
        # looked up in the next call to rename_tier1_files.
        population_ordered = new_population_ordered

    # ========== FINAL SUMMARY ==========
    logger.info("\n" + "=" * 80)
    logger.info("GENETIC ALGORITHM COMPLETED SUCCESSFULLY!")
    logger.info("=" * 80)
    logger.info(f"Total generations    : {cfg['generations']}")
    logger.info(f"Total PDBs generated : {cfg['generations'] * cfg['population_size']}")
    logger.info(f"Results in           : {output_dir.absolute()}")
    logger.info(f"SANS data in         : {primus_dir}")
    logger.info(f"Best fitness summary : {best_summary_path.absolute()}")

    if sorted_pop:
        best = sorted_pop[0]
        logger.info(f"\n>>> Best solution found:")
        logger.info(f"  D2O: {best.d2o}%")
        logger.info(f"  Deuterated amino acids: {sum(best.deuteration)}/20")
        logger.info(f"  Fitness: {best.fitness:.6f}")
        logger.info(f"  PDB: {generate_pdb_filename(best, 1, cfg['generations'] - 1)}")

    logger.info("=" * 80)


if __name__ == "__main__":
    try:
        start = time.perf_counter()
        main()
        end = time.perf_counter()
        print(f"Execution time: {end - start:.6f} seconds")
    except KeyboardInterrupt:
        logger.warning("\nInterrupted by user")
        sys.exit(1)
    except Exception as e:
        logger.exception(f"Fatal error: {e}")
        sys.exit(1)
