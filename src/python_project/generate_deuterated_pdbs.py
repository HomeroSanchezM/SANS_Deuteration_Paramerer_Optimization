def generate_pdbs_for_chromosomes(pdb_file: str,
                                   chromosomes: List[Chromosome],
                                   output_dir: Path) -> List[str]:
    """
    Generate a deuterated PDB file for each chromosome in *chromosomes*.

    The filename is derived from the chromosome's own generation/index attributes
    via :func:`get_pdb_filename`, so no external index tracking is needed.

    Args:
        pdb_file:    Source (protonated) PDB file.
        chromosomes: Chromosomes to generate PDBs for.
        output_dir:  Directory in which to write the PDB files.

    Returns:
        List of absolute paths (strings) of the generated PDB files.
    """
    logger.info(f"Generating {len(chromosomes)} deuterated PDB file(s)...")
    generated = []
    for chrom in chromosomes:
        filename = get_pdb_filename(chrom)
        out_path = output_dir / filename
        try:
            deuterator = PdbDeuteration(pdb_file)
            deuterator.apply_deuteration(chrom.deuteration, chrom.d2o)
            chrom.H = deuterator.stats['hydrogen_atoms']
            chrom.D = deuterator.stats['deuterium_atoms']
            print("For protonated in D2O there are :")
            print(f"{deuterator.stats['hydrogen_atoms']} hydrogen atoms")
            print(f"{deuterator.stats['deuterium_atoms']} deuterium atoms")
            print(f"The %D is {(deuterator.stats['deuterium_atoms'] / (deuterator.stats['deuterium_atoms'] + deuterator.stats['hydrogen_atoms'])) * 100:.2f}")
            deuterator.save(str(out_path))
            generated.append(str(out_path))
            logger.debug(f"  Written: {filename}")
        except Exception as exc:
            logger.error(f"  FAILED {filename}: {exc}")
    logger.info(f"  {len(generated)} PDB file(s) written.")
    return generated


