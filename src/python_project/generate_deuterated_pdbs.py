def parse_arguments():
    """Parse command-line arguments for the genetic algorithm workflow."""
    parser = argparse.ArgumentParser(
        description=(
            "Generate deuterated PDBs using a genetic algorithm "
            "and evaluate fitness via Pepsi-SANS"
        ),
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__
    )

    # ---- Positional ----
    parser.add_argument('pdb_file', type=str, help='Source PDB file to deuterate')

    # ---- Config file ----
    parser.add_argument(
        '--config', type=str, metavar='FILE',
        help='Configuration INI file (CLI arguments override INI)'
    )

    # ---- I/O ----
    io = parser.add_argument_group('Input/Output')
    io.add_argument(
        '--output_dir', type=str,
        help='Output directory (default: <pdb_basename>_deuterated_pdbs)'
    )
    io.add_argument(
        '--batch_script', type=str, default='./process_pdb.sh',
        help='Path to the batch processing script (default: ./process_pdb.sh)'
    )

    # ---- Population ----
    pop = parser.add_argument_group('Population parameters')
    pop.add_argument('-p', '--population_size', type=int,
                     help='Population size (MUST be a multiple of 3). Default: 6')
    pop.add_argument('-e', '--elitism', type=int,
                     help='Elite individuals preserved (≤ population_size/3). Default: 2')
    pop.add_argument('--d2o-var', '--d2o_variation_rate', dest='d2o_variation_rate',
                     type=int, help='Max D2O variation amplitude (0-100). Default: 5')
    pop.add_argument('--d2o', type=int, nargs='+', default=None, metavar='VALUE',
                     help=(
                         'Lock D2O to a fixed list of values (e.g. --d2o 0 42 100). '
                         'When set, d2o_variation_rate is ignored and every chromosome '
                         'draws its D2O uniformly from this list.'
                     ))

    # ---- Genetic ----
    gen = parser.add_argument_group('Genetic parameters')
    gen.add_argument('-m', '--mutation_rate', type=float,
                     help='Mutation rate (0.0-1.0). Default: 0.15')
    gen.add_argument('-c', '--crossover_rate', type=float,
                     help='Crossover rate (0.0-1.0). Default: 0.8')

    # ---- Execution ----
    exc = parser.add_argument_group('Execution parameters')
    exc.add_argument('-g', '--generations', type=int,
                     help='Number of generations. Default: 1')
    exc.add_argument('--seed', type=int,
                     help='Random seed for reproducibility. Default: 1')

    # ---- Fitness ----
    fit = parser.add_argument_group('Fitness evaluation parameters')
    fit.add_argument('--q-max', type=float,
                     help='Max q value for truncation (Å⁻¹). Default: 0.3')
    fit.add_argument('--ratio-threshold', type=float,
                     help='Min Imax/background ratio to accept a curve. Default: 0.01')
    fit.add_argument('--i0-threshold', type=float, help=argparse.SUPPRESS)
    fit.add_argument('--deut-ref', type=str,
                     help='Exact filename of deuterated reference inside ref/ (auto-detected if not given)')
    fit.add_argument('--prot-ref', type=str,
                     help='Exact filename of protonated reference inside ref/ (auto-detected if not given)')

    # ---- Verbosity ----
    parser.add_argument('--verbose', action='store_true', help='Enable debug logging')

    args = parser.parse_args()
    if args.verbose:
        logger.setLevel(logging.DEBUG)

    return args


