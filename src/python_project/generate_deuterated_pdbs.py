def create_output_directory(output_dir: Optional[str], pdb_file: str) -> Tuple[Path, Path]:
    """Create output directory and ref subdirectory."""
    if output_dir is None:
        output_path = Path(pdb_file).stem + "_deuterated_pdbs"
        output_path = Path(output_path)
    else:
        output_path = Path(output_dir)

    output_path.mkdir(exist_ok=True, parents=True)
    ref_path = output_path / "ref"
    ref_path.mkdir(exist_ok=True)
    logger.info(f"PDB Output directory: {output_path.absolute()}")
    return output_path, ref_path


def generate_pdb_filename(chromosome: Chromosome, index: int, generation: int = 0) -> str:
    """Generate a descriptive PDB filename."""
    num_deut = sum(chromosome.deuteration)
    return f"gen{generation:02d}_chr{index:03d}_d2o{chromosome.d2o:02d}_deutAA{num_deut:02d}.pdb"


def save_population_summary(population: List[Chromosome],
                            output_dir: Path,
                            generation: int = 0) -> None:
    """Save a detailed summary of the population to a text file."""
    summary_file = output_dir / f"generation_{generation:02d}_summary.txt"
    with open(summary_file, 'w', encoding='utf-8') as f:
        f.write("=" * 80 + "\n")
        f.write(f"POPULATION SUMMARY - GENERATION {generation}\n")
        f.write("=" * 80 + "\n\n")
        f.write(f"Population size: {len(population)}\n")

        fitness_values = [c.fitness for c in population if c.fitness is not None]
        if fitness_values:
            f.write(f"Average fitness: {np.mean(fitness_values):.6f}\n")
            f.write(f"Best fitness   : {np.max(fitness_values):.6f}\n")
            f.write(f"Worst fitness  : {np.min(fitness_values):.6f}\n")

        f.write("\n" + "-" * 80 + "\n")
        f.write(f"{'#':<5} {'PDB Filename':<45} {'D2O%':<8} {'AA deut.':<12} {'Fitness':<10}\n")
        f.write("-" * 80 + "\n")
        for i, chrom in enumerate(population, 1):
            filename = generate_pdb_filename(chrom, i, generation) #les nom des fichier sont erronnees
            num_deut = sum(chrom.deuteration)
            fit_str = f"{chrom.fitness:.6f}" if chrom.fitness is not None else "N/A"
            f.write(f"{i:<5} {filename:<45} {chrom.d2o:<8} {num_deut:<12} {fit_str:<10}\n")
        f.write("-" * 80 + "\n\n")
        f.write("=" * 80 + "\n")
    logger.info(f"Summary saved: {summary_file}")


def save_best_fitness_summary(best_chrom: Chromosome,
                              generation: int,
                              summary_path: Path) -> None:
    """
    Append the best chromosome record for this generation to the best fitness summary CSV.

    The CSV contains one row per generation with columns:
        - generation       : generation index
        - fitness          : best raw fitness (area difference)
        - d2o_percent      : D2O percentage of the best chromosome
        - n_deuterated_aa  : number of deuterated amino acid types
        - deuterated_aa_list: semicolon-separated 3-letter codes of deuterated AAs

    Args:
        best_chrom (Chromosome): Best chromosome of this generation
        generation (int): Generation index
        summary_path (Path): Path to the CSV file (created if it does not exist)
    """
    deut_aas = [
        AMINO_ACIDS[i].code_3
        for i, deuterated in enumerate(best_chrom.deuteration)
        if deuterated
    ]
    deut_aa_str = ";".join(deut_aas) if deut_aas else "none"

    write_header = not summary_path.exists()
    with open(summary_path, 'a', encoding='utf-8') as f:
        if write_header:
            f.write("generation,fitness,d2o_percent,n_deuterated_aa,deuterated_aa_list\n")
        f.write(
            f"{generation},{best_chrom.fitness:.8f},"
            f"{best_chrom.d2o},{sum(best_chrom.deuteration)},{deut_aa_str}\n"
        )
    logger.info(
        f"Best fitness summary updated: {summary_path.name} "
        f"(gen {generation}: fitness={best_chrom.fitness:.6f}, "
        f"D2O={best_chrom.d2o}%, AAs=[{deut_aa_str}])"
    )


def run_batch_processing(output_dir: Path, batch_script: str) -> str:
    """Execute the external batch script for SANS simulation."""
    logger.info("=" * 80)
    logger.info(">>> Running batch processing (Pepsi-SANS simulation)...")
    logger.info("=" * 80)
    try:
        result = subprocess.run(
            [batch_script, str(output_dir)],
            check=True,
            capture_output=True,
            text=True
        )
        logger.debug(result.stdout)
        # Infer primus output directory (same logic as the batch script)
        folder_name = output_dir.name
        prefix = folder_name.replace("_deuterated_pdbs", "")
        primus_dir = output_dir.parent / f"{prefix}_primus_out"
        logger.info(f"SANS data generated in: {primus_dir}")
        return str(primus_dir)
    except subprocess.CalledProcessError as e:
        logger.error(f"Batch processing failed (code {e.returncode})")
        logger.error(e.stderr)
        raise RuntimeError(f"Batch script error: {e}")
    except FileNotFoundError:
        raise RuntimeError(f"Batch script not found: {batch_script}")


def evaluate_fitness(primus_output_dir: str,
                     population: List[Chromosome],
                     q_max: float,
                     ratio_threshold: float,
                     deut_ref: Optional[str] = None,
                     prot_ref: Optional[str] = None) -> None:
    """
    Evaluate fitness of all chromosomes using SANS data.

    The dat files returned by evaluate_population_fitness are sorted alphabetically,
    which matches the chromosome order in population (population[i] was written as
    chr{i+1}, so alphabetical order of filenames == population order).
    """
    logger.info("=" * 80)
    logger.info(">>> Evaluating population fitness from SANS data...")
    logger.info("=" * 80)
    try:
        fitness_scores, dat_files = evaluate_population_fitness(
            directory=primus_output_dir,
            deut_ref=deut_ref,
            prot_ref=prot_ref,
            q_max=q_max,
            ratio_threshold=ratio_threshold
        )
        # Assign scores to chromosomes
        min_len = min(len(fitness_scores), len(population))
        for i in range(min_len):
            population[i].fitness = float(fitness_scores[i])
            #test avec bonne fitness tout le temp
            #population[i].fitness = 1.0
        logger.info(f"Fitness evaluation complete. "
                    f"Passed ratio check: {np.sum(fitness_scores > 0)}/{len(fitness_scores)}")
    except Exception as e:
        logger.error(f"Fitness evaluation failed: {e}")
        raise RuntimeError(f"Fitness evaluation error: {e}")


def rename_tier1_files(tier1: List[Chromosome],
                       prev_population_ordered: List[Chromosome],
                       output_dir: Path,
                       primus_dir: Optional[str],
                       prev_gen: int,
                       new_gen: int) -> None:
    """
    Rename PDB (and SANS output) files for tier1 chromosomes from prev_gen to new_gen.

    IMPORTANT: The lookup is done against prev_population_ordered, which is the population
    in the EXACT order it was passed to generate_deuterated_pdbs() in the previous generation.
    This order directly corresponds to the chr index used in filenames:
        prev_population_ordered[0]  → gen{prev_gen}_chr001_...
        prev_population_ordered[1]  → gen{prev_gen}_chr002_...
        ...
    Using sorted_pop instead would give WRONG indices because sorting changes the order.

    For each chromosome in tier1:
    - Find its position in prev_population_ordered (= old file index)
    - Rename gen{prev_gen}_chr{old_idx}_* to gen{new_gen}_chr{new_idx}_*
    - Also rename the corresponding SANS output files (.dat / .out)

    Duplicate chromosomes in tier1 (allowed by probabilistic selection) are skipped
    after the first occurrence; they will have their PDB regenerated.

    Args:
        tier1 (List[Chromosome]): Tier1 chromosomes of the new generation
        prev_population_ordered (List[Chromosome]): Previous generation population in the
            ORIGINAL order used when PDB files were generated (NOT the sorted order)
        output_dir (Path): Directory containing PDB files
        primus_dir (str or None): Directory containing SANS output (.dat/.out) files
        prev_gen (int): Previous generation index
        new_gen (int): New generation index
    """
    # Track which chromosomes have already been renamed (to handle duplicates in tier1)
    already_renamed: set = set()

    for new_idx, chrom in enumerate(tier1, 1):
        chrom_key = (tuple(chrom.deuteration), chrom.d2o)

        # Skip duplicates: they will be regenerated from scratch
        if chrom_key in already_renamed:
            logger.debug(
                f"Tier1 chr{new_idx:03d}: duplicate chromosome in tier1, will be regenerated"
            )
            continue

        # Find the chromosome in the ORDERED previous population.
        # The 1-based position here directly maps to the chr index in filenames.
        old_idx = None
        for j, prev_chrom in enumerate(prev_population_ordered):
            if prev_chrom.deuteration == chrom.deuteration and prev_chrom.d2o == chrom.d2o:
                old_idx = j + 1  # 1-based index == chr number in filename
                break

        if old_idx is None:
            logger.warning(
                f"Tier1 chr{new_idx:03d}: chromosome not found in previous generation's "
                f"ordered population; PDB will be regenerated"
            )
            continue

        num_deut = sum(chrom.deuteration)
        old_stem = (
            f"gen{prev_gen:02d}_chr{old_idx:03d}"
            f"_d2o{chrom.d2o:02d}_deutAA{num_deut:02d}"
        )
        new_stem = (
            f"gen{new_gen:02d}_chr{new_idx:03d}"
            f"_d2o{chrom.d2o:02d}_deutAA{num_deut:02d}"
        )

        # Rename PDB file
        old_pdb = output_dir / f"{old_stem}.pdb"
        new_pdb = output_dir / f"{new_stem}.pdb"
        if old_pdb.exists():
            old_pdb.rename(new_pdb)
            logger.debug(f"Renamed PDB: {old_stem}.pdb -> {new_stem}.pdb")
        else:
            logger.warning(f"PDB not found for renaming: {old_pdb.name}")

        # Rename SANS output files if primus_dir is available
        if primus_dir:
            primus_path = Path(primus_dir)
            for ext in ['.dat', '.out']:
                old_sans = primus_path / f"{old_stem}{ext}"
                new_sans = primus_path / f"{new_stem}{ext}"
                if old_sans.exists():
                    old_sans.rename(new_sans)
                    logger.debug(f"Renamed SANS: {old_sans.name} -> {new_sans.name}")

        already_renamed.add(chrom_key)

    renamed_count = len(already_renamed)
    logger.info(
        f"Tier1 file renaming: {renamed_count} unique chromosome(s) renamed "
        f"from gen{prev_gen:02d} to gen{new_gen:02d}"
    )


def cleanup_non_tier1_files(output_dir: Path,
                             primus_dir: Optional[str],
                             generation: int) -> None:
    """
    Remove all remaining PDB and SANS output files from a given generation.

    This function is called after tier1 files have already been renamed to the new
    generation, so only the non-tier1 files remain under the old generation prefix.

    Args:
        output_dir (Path): Directory containing PDB files
        primus_dir (str or None): Directory containing SANS output files
        generation (int): Generation index whose leftover files should be removed
    """
    pattern = f"gen{generation:02d}_*.pdb"
    removed_pdb = 0
    for pdb in output_dir.glob(pattern):
        try:
            pdb.unlink()
            removed_pdb += 1
        except Exception as e:
            logger.warning(f"Could not remove {pdb.name}: {e}")
    logger.info(f"Removed {removed_pdb} non-tier1 PDB files from generation {generation}")

    if primus_dir:
        primus_path = Path(primus_dir)
        removed_sans = 0
        for ext in ['.dat', '.out']:
            for f in primus_path.glob(f"gen{generation:02d}_*{ext}"):
                try:
                    f.unlink()
                    removed_sans += 1
                except Exception as e:
                    logger.warning(f"Could not remove {f.name}: {e}")
        if removed_sans:
            logger.info(
                f"Removed {removed_sans} non-tier1 SANS output files from generation {generation}"
            )


def generate_deuterated_pdbs(pdb_file: str,
                             population: List[Chromosome],
                             output_dir: Path,
                             generation: int = 0,
                             start_index: int = 1) -> None:
    """
    Generate deuterated PDB files for chromosomes in the population.

    The chromosome at position i in population receives the filename index
    start_index + i, i.e.:
        population[0] → gen{generation}_chr{start_index:03d}_...
        population[1] → gen{generation}_chr{start_index+1:03d}_...
        ...

    This mapping must be preserved so that rename_tier1_files can correctly
    look up file indices from the ordered population list.

    Args:
        pdb_file (str): Source PDB file
        population (List[Chromosome]): Chromosomes to generate PDBs for
        output_dir (Path): Output directory
        generation (int): Generation index (used in filename)
        start_index (int): Starting chromosome index for filename numbering (default: 1)
    """
    logger.info(f"Generating {len(population)} deuterated PDB files (gen {generation})...")
    for i, chrom in enumerate(population, start_index):
        try:
            deuterator = PdbDeuteration(pdb_file)
            deuterator.apply_deuteration(chrom.deuteration, chrom.d2o)
            filename = generate_pdb_filename(chrom, i, generation)
            out_path = output_dir / filename
            deuterator.save(str(out_path))
            logger.debug(f"  [{i:03d}] {filename}")
        except Exception as e:
            logger.error(f"  [{i:03d}] FAILED: {e}")
    logger.info(f"Generation {generation} PDBs created.")


def create_reference_pdbs(pdb_file: str,
                          ref_dir: Path,
                          restrictions: List[bool]) -> None:
    """
    Create fully deuterated and fully protonated reference PDBs.
    restriction are not consider for this reference files
    """
    logger.info(">>> Creating reference PDBs (fully deuterated / protonated)")
    no_restrictions = [True] * len(AMINO_ACIDS)
    # Fully deuterate
    deut_chrom = Chromosome(AMINO_ACIDS, no_restrictions)
    deut_chrom.deuteration = [True] * len(AMINO_ACIDS)
    deut_chrom.d2o = 100
    deut_pdb = PdbDeuteration(pdb_file)
    deut_pdb.apply_deuteration(deut_chrom.deuteration, deut_chrom.d2o)
    # Fix: use f-string with Path / operator
    deut_filename = f"{Path(pdb_file).stem}_total_deuteration.pdb"
    deut_path = ref_dir / deut_filename
    deut_pdb.save(str(deut_path))
    logger.info(f"  Deuterated reference: {deut_path.name}")

    # Fully protonated
    prot_chrom = Chromosome(AMINO_ACIDS, no_restrictions)
    prot_chrom.deuteration = [False] * len(AMINO_ACIDS)
    prot_chrom.d2o = 0
    prot_pdb = PdbDeuteration(pdb_file)
    prot_pdb.apply_deuteration(prot_chrom.deuteration, prot_chrom.d2o)
    prot_filename = f"{Path(pdb_file).stem}_total_protonation.pdb"
    prot_path = ref_dir / prot_filename
    prot_pdb.save(str(prot_path))
    logger.info(f"  Protonated reference: {prot_path.name}")


def display_population_summary(population: List[Chromosome], generation: int) -> None:
    """Print top chromosomes and basic statistics."""
    sorted_pop = sorted(population, key=lambda x: x.fitness if x.fitness else 0, reverse=True)
    logger.info(f"\n>>> Generation {generation} - Top 3 chromosomes:")
    for i in range(min(3, len(sorted_pop))):
        c = sorted_pop[i]
        fit_str = f"{c.fitness:.4f}" if c.fitness else "N/A"
        logger.info(f"  {i + 1}. D2O={c.d2o:2d}%, AA deut={sum(c.deuteration):2d}/20, Fitness={fit_str}")


