def evaluate_fitness(primus_dir: str,
                     population: List[Chromosome],
                     q_max: float,
                     ratio_threshold: float,
                     deut_ref: Optional[str] = None,
                     prot_ref: Optional[str] = None) -> None:
    """
    Evaluate fitness for all chromosomes by matching .dat filenames.

    The function calls :func:`evaluate_population_fitness` which returns raw
    fitness scores for every .dat file found in *primus_dir* (alphabetical
    order).  Each score is then mapped back to the corresponding chromosome
    via the chromosome's expected filename stem.

    Tier-1 chromosomes already have valid .dat files from a previous run;
    their fitness is re-read from those unchanged files (values are identical).

    Args:
        primus_dir:       Path to the Pepsi-SANS output directory.
        population:       Full current population (tier-1 + tier-2 + tier-3).
        q_max:            q truncation limit.
        ratio_threshold:  Minimum Imax/background ratio threshold.
        deut_ref:         Exact deuterated reference filename (or None for auto-detect).
        prot_ref:         Exact protonated reference filename (or None for auto-detect).
    """
    logger.info("=" * 70)
    logger.info(">>> Evaluating fitness from SANS data…")
    logger.info("=" * 70)

    # Build stem → chromosome lookup (one entry per chromosome)
    chrom_by_stem: Dict[str, Chromosome] = {}
    for chrom in population:
        stem = Path(get_pdb_filename(chrom)).stem
        chrom_by_stem[stem] = chrom

    try:
        fitness_scores, dat_files = evaluate_population_fitness(
            directory=primus_dir,
            deut_ref=deut_ref,
            prot_ref=prot_ref,
            q_max=q_max,
            ratio_threshold=ratio_threshold,
        )
    except Exception as exc:
        logger.error(f"Fitness evaluation failed: {exc}")
        raise RuntimeError(f"Fitness evaluation error: {exc}") from exc

    matched = 0
    unmatched_files = []
    for score, file_path in zip(fitness_scores, dat_files):
        stem = Path(file_path).stem
        if stem in chrom_by_stem:
            chrom_by_stem[stem].fitness = float(score)
            matched += 1
        else:
            unmatched_files.append(Path(file_path).name)

    if unmatched_files:
        logger.warning(
            f"  {len(unmatched_files)} .dat file(s) had no matching chromosome: "
            + ", ".join(unmatched_files[:5])
            + (" …" if len(unmatched_files) > 5 else "")
        )

    chromosomes_without_fitness = [
        chrom for chrom in population
        if chrom.fitness == 0.0 and
        Path(get_pdb_filename(chrom)).stem not in {Path(f).stem for f in dat_files}
    ]
    if chromosomes_without_fitness:
        logger.warning(
            f"  {len(chromosomes_without_fitness)} chromosome(s) have no .dat output "
            f"(fitness kept at 0.0)"
        )

    passed = int(np.sum(fitness_scores > 0))
    logger.info(
        f"  Matched {matched}/{len(population)} chromosome(s). "
        f"Passed ratio check: {passed}/{len(fitness_scores)}"
    )


