def display_population_summary(population: List[Chromosome],
                                sorted_indices: List[int],
                                generation: int) -> None:
    """Log a short summary of the top-3 chromosomes using their original filenames."""
    top_n = min(10, len(sorted_indices))
    logger.info(f"\n>>> Generation {generation} — top {top_n} chromosome(s):")
    for rank in range(top_n):
        chrom = population[sorted_indices[rank]]
        logger.info(
            f"  {rank + 1:2d}. {get_pdb_filename(chrom)}"
            f"  fitness={chrom.fitness:.6f}"
            f"  H={chrom.H}"
            f"  D={chrom.D}"
            f"  %D={(chrom.D/(chrom.H + chrom.D))*100 :.2f}"
            f"  (created gen{chrom.generation:02d} idx{chrom.index:03d})"
        )


def save_population_summary(population: List[Chromosome],
                             sorted_indices: List[int],
                             output_dir: Path,
                             generation: int) -> None:
    """
    Save a detailed ranked summary of the population to a text file.

    Chromosomes are listed in descending fitness order using their original
    (creation) filenames so that every row can be traced back to a specific
    PDB / SANS file.
    """
    summary_file = output_dir / f"generation_{generation:02d}_summary.txt"
    fitness_values = [population[i].fitness for i in sorted_indices
                      if population[i].fitness is not None]

    with open(summary_file, 'w', encoding='utf-8') as fh:
        fh.write("=" * 90 + "\n")
        fh.write(f"POPULATION SUMMARY — GENERATION {generation}\n")
        fh.write("=" * 90 + "\n\n")
        fh.write(f"Population size : {len(population)}\n")
        if fitness_values:
            fh.write(f"Best fitness    : {max(fitness_values):.6f}\n")
            fh.write(f"Average fitness : {float(np.mean(fitness_values)):.6f}\n")
            fh.write(f"Worst fitness   : {min(fitness_values):.6f}\n")
        fh.write("\n")
        fh.write(
            f"{'Rank':<6} {'PDB filename (creation name)':<55} "
            f"{'D2O%':<6} {'AA':<5} {'Fitness':<14} {'Created':<12}\n"
        )
        fh.write("-" * 90 + "\n")
        for rank, idx in enumerate(sorted_indices, 1):
            chrom = population[idx]
            filename = get_pdb_filename(chrom)
            fit_str = f"{chrom.fitness:.6f}" if chrom.fitness is not None else "N/A"
            fh.write(
                f"{rank:<6} {filename:<55} {chrom.d2o:<6} "
                f"{sum(chrom.deuteration):<5} {fit_str:<14} "
                f"gen{chrom.generation:02d}_idx{chrom.index:03d}\n"
            )
        fh.write("=" * 90 + "\n")

    logger.info(f"  Summary saved : {summary_file.name}")


def save_best_fitness_summary(best_chrom: Chromosome,
                               generation: int,
                               summary_path: Path) -> None:
    """
    Append the best chromosome of the current generation to a CSV file.

    Columns:
        generation, fitness, d2o_percent, n_deuterated_aa,
        deuterated_aa_list, pdb_filename,
        created_generation, created_index
    """
    deut_aas = [AMINO_ACIDS[i].code_3
                for i, d in enumerate(best_chrom.deuteration) if d]
    deut_str = ";".join(deut_aas) if deut_aas else "none"
    filename = get_pdb_filename(best_chrom)

    write_header = not summary_path.exists()
    with open(summary_path, 'a', encoding='utf-8') as fh:
        if write_header:
            fh.write(
                "generation,fitness,d2o_percent,n_deuterated_aa,"
                "deuterated_aa_list,pdb_filename,"
                "created_generation,created_index\n"
            )
        fh.write(
            f"{generation},{best_chrom.fitness:.8f},{best_chrom.d2o},"
            f"{sum(best_chrom.deuteration)},{deut_str},{filename},"
            f"{best_chrom.generation},{best_chrom.index}\n"
        )
    logger.info(
        f"  Best fitness CSV updated: gen={generation}, "
        f"fitness={best_chrom.fitness:.6f}, "
        f"D2O={best_chrom.d2o}%, "
        f"file={filename}"
    )


# ============================================================================
#                       ELITISM GUARD
# ============================================================================

def check_fitness_non_decreasing(population: List[Chromosome],
                                  sorted_indices: List[int],
                                  previous_best: Optional[float],
                                  generation: int) -> float:
    """
    Verify that the best fitness is non-decreasing across generations.

    With elitism > 0, the best individual from the previous generation is
    always preserved in tier-1, so the best fitness must never decrease.
    A warning is logged if this invariant is violated.

    Args:
        population:     Current generation's population.
        sorted_indices: Indices sorted by descending fitness.
        previous_best:  Best fitness of the previous generation (None for gen 0).
        generation:     Current generation number (for log messages).

    Returns:
        The current generation's best fitness value.
    """
    current_best = population[sorted_indices[0]].fitness

    if previous_best is None:
        logger.info(f"  Best fitness (gen {generation}): {current_best:.6f}")
    elif current_best < previous_best - 1e-10:
        logger.warning(
            f"  ELITISM VIOLATION at generation {generation}: "
            f"best fitness decreased from {previous_best:.6f} to {current_best:.6f}. "
            f"Check elitism setting and file management."
        )
    else:
        delta = current_best - previous_best
        logger.info(
            f"  Best fitness (gen {generation}): {current_best:.6f} "
            f"(Δ = {delta:+.6f} vs gen {generation - 1})"
        )

    return current_best


